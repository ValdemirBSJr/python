numero1 = int(input("Digite um número..:"))

numero2 = int(input("Digite o segundo número..:"))

if numero1 == numero2:
    print("O número ", numero1, "é igual a ", numero2)
if (numero1 != numero2):
    print("O número %d é diferente do número %d." %(numero1, numero2))
if numero1 < numero2:
    print("O número %d é menor do que o número %d." %(numero1, numero2))
if numero1 > numero2:
    print("O número %d é maior do que o número %d." %(numero1, numero2))
if numero1 <= numero2:
    print("O número %d é menor ou igual do que o número %d." %(numero1, numero2))
if numero1 >= numero2:
    print("O número %d é maior ou igual do que o número %d." %(numero1, numero2))